weather_today = 'sunny'

case weather_today
when 'sunny'
	puts "Shorts and sunglasses are needed today."
when 'rainy'
	puts "Grab a raincoat and umbrella."
when 'foggy'
	puts "You'll need a hat for all this dreariness."
when 'cloudy'
	puts "No sunglassess needed today!"
else 
	puts "I am not equipped to give you that kind of advice."
end
		